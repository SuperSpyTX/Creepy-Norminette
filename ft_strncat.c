#include "libft.h"

char				*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t				g;
	size_t				k;

	g = -1;
	k = 0;
	while (s1[++g] != '\0')
		;
	while (k < n && (s1[g++] = s2[k++]) != '\0')
		;
	return (s1);
}
