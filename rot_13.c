void			ft_rot13(char *ptr)
{
	while (*ptr)
	{
		if ((*ptr >= 'a' && *ptr <= 'z'))
			*ptr = (((((int)*ptr + 13) - 'a') % 26) + 'a');
		if ((*ptr >= 'A' && *ptr <= 'Z'))
			*ptr = (((((int)*ptr + 13) - 'A') % 26) + 'A');
		ptr += 1;
	}
}
